package personal_code_1;

public class MultiplicationTable {
	public static void main(String[] args) {
		for( int i = 2; i < 3; i++) {
			for(int j = 1; j < 3; j++) {
				System.out.printf("\n%d X %d = %d",i, j, i*j);
			}
		}
		for( int i = 4; i < 5; i++) {
			for(int j = 1; j < 5; j++) {
				System.out.printf("\n%d X %d = %d",i, j, i*j);
			}
		}
				

			
	}

}
